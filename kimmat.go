package main
import "fmt"
func main() {

    fmt.Println("go" + "teli") // bu go telidiki string yanai 
    //harip balgilar tipip bolup harip baliga munasiwatlik barlik san sifirlar muxu tip da bir tarap kilinidu

    fmt.Println("1+1 =", 1+1) // bu go telidiki integer putun san tipip bolup
    // putun sanga munaswaitlik barlik uqurlar muxu tipda bir tarap kilinidu
   


    fmt.Println("7.0/3.0 =", 7.0/3.0)// bu go telidki float yani laylima san tipip bolup laylima 
    // sanga munasiwatlik barlik uqurlar muxu tipda bir tarap kilinidu

    fmt.Println(true && false)// bu go telidki boolean yani ras yalganga hokum kilix tipip bolup 
    // logilik uqurlar muxu tipda bir tarap kilinidu
    fmt.Println(true || false)
    fmt.Println(!true)
}